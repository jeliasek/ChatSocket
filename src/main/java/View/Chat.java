    public void acoes(){
        btnEnviar.addActionListener(event -> enviarMensagem());
        tfMensagem.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                
            }

            @Override
            public void keyPressed(KeyEvent e) {
                if(e.getKeyChar() == KeyEvent.VK_ENTER) {
                    enviarMensagem();
                }
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        });
        this.addWindowListener(new WindowListener() {
            @Override
            public void windowOpened(WindowEvent e) {
                
            }

            @Override
            public void windowClosing(WindowEvent e) {
                Utils.enviarMensagem(conn, "FECHAR_CHAT");
                home.getChatsAbertos().remove(dadosConexao);
                home.getConectadosListeners().remove(dadosConexao);
                home.getConectadosListeners().get(dadosConexao).setIsChatOpen(false);
                home.getConectadosListeners().get(dadosConexao).setIsRunning(false);
                
            }

            @Override
            public void windowClosed(WindowEvent e) {
                
            }

            @Override
            public void windowIconified(WindowEvent e) {
                
            }

            @Override
            public void windowDeiconified(WindowEvent e) {
                
            }

            @Override
            public void windowActivated(WindowEvent e) {
                
            }

            @Override
            public void windowDeactivated(WindowEvent e) {
                
            }
        });
    }

    public void incrementarMensagem(String mensagem) {
        mensagens.add(mensagem);
        String msg = "";
        for (String str : mensagens) {
            msg += str;
        }
        listaMensagens.setText(msg);
    }

    private void enviarMensagem() {
        if(tfMensagem.getText().length() > 0)
        {
            DateFormat df = new SimpleDateFormat("hh:mm:ss");
            Utils.enviarMensagem(conn, "MENSAGEM;" + "<b>[" + df.format(new Date()) + "] "+this.titulo+": </b><i>" + tfMensagem.getText() + "</i><br>");
            incrementarMensagem("<b>[" + df.format(new Date()) + "] Eu: </b><i>" + tfMensagem.getText() + "</i><br>");
            tfMensagem.setText("");
        }
    }

